// https://leetcode.com/problems/max-area-of-island/

var maxAreaOfIsland = function (grid) {
  const visitedCells = {};
  const rows = grid.length;
  const cols = grid[0].length;
  let res = 0;

  const DIRS = [
    [0, 1],
    [1, 0],
    [-1, 0],
    [0, -1],
  ];

  for (let i = 0; i < rows; i++) {
    for (let j = 0; j < cols; j++) {
      let currentCell = grid[i][j];
      let currentIdentifier = `${i}-${j}`;

      if (visitedCells[currentIdentifier] || currentCell == 0) {
        continue;
      }

      let currentStepLandSize = 0;

      visitedCells[currentIdentifier] = true;

      const stack = [[i, j]];

      while (stack.length) {
        const currentEl = stack.pop();

        currentStepLandSize++;

        for (let d of DIRS) {
          let newR = currentEl[0] + d[0];
          let newC = currentEl[1] + d[1];

          if (
            newR >= 0 &&
            newR < rows &&
            newC >= 0 &&
            newC < cols &&
            grid[newR][newC] == 1 &&
            !visitedCells[`${newR}-${newC}`]
          ) {
            stack.push([newR, newC]);
            visitedCells[`${newR}-${newC}`] = true;
          }
        }
      }

      res = Math.max(res, currentStepLandSize);
    }
  }

  return res;
};

